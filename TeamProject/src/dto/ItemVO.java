package dto;

public class ItemVO {
	private Integer item_num;
	private String item_category;
	private String item_name;
	private String item_content;
	private Integer item_price;
	private Integer item_quantity;
	private String item_date;
	private String item_total;
	private String item_time;

	public Integer getItem_num() {
		return item_num;
	}

	public void setItem_num(Integer item_num) {
		this.item_num = item_num;
	}

	public String getItem_category() {
		return item_category;
	}

	public void setItem_category(String item_category) {
		this.item_category = item_category;
	}

	public String getItem_name() {
		return item_name;
	}

	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}

	public String getItem_content() {
		return item_content;
	}

	public void setItem_content(String item_content) {
		this.item_content = item_content;
	}

	public Integer getItem_price() {
		return item_price;
	}

	public void setItem_price(Integer item_price) {
		this.item_price = item_price;
	}

	public Integer getItem_quantity() {
		return item_quantity;
	}

	public void setItem_quantity(Integer item_quantity) {
		this.item_quantity = item_quantity;
	}

	public String getItem_date() {
		return item_date;
	}

	public void setItem_date(String item_date) {
		this.item_date = item_date;
	}

	public String getItem_total() {
		return item_total;
	}

	public void setItem_total(String item_total) {
		this.item_total = item_total;
	}

	public String getItem_time() {
		return item_time;
	}

	public void setItem_time(String item_time) {
		this.item_time = item_time;
	}

}
