package dto;

public class PostScriptVO {
	private Integer post_num;
	private String post_subject;
	private String post_writer;
	private String post_date;
	private Integer post_help;
	private Integer post_hits;
	private Integer post_stars;
	private String post_content;

	public Integer getPost_num() {
		return post_num;
	}

	public void setPost_num(Integer post_num) {
		this.post_num = post_num;
	}

	public String getPost_subject() {
		return post_subject;
	}

	public void setPost_subject(String post_subject) {
		this.post_subject = post_subject;
	}

	public String getPost_writer() {
		return post_writer;
	}

	public void setPost_writer(String post_writer) {
		this.post_writer = post_writer;
	}

	public String getPost_date() {
		return post_date;
	}

	public void setPost_date(String post_date) {
		this.post_date = post_date;
	}

	public Integer getPost_help() {
		return post_help;
	}

	public void setPost_help(Integer post_help) {
		this.post_help = post_help;
	}

	public Integer getPost_hits() {
		return post_hits;
	}

	public void setPost_hits(Integer post_hits) {
		this.post_hits = post_hits;
	}

	public Integer getPost_stars() {
		return post_stars;
	}

	public void setPost_stars(Integer post_stars) {
		this.post_stars = post_stars;
	}

	public String getPost_content() {
		return post_content;
	}

	public void setPost_content(String post_content) {
		this.post_content = post_content;
	}

}
