package dto;

public class PostScriptDAO {
	private Integer item_num;
	private String item_subject;
	private String item_writer;
	private String item_date;
	private Integer item_help;
	private Integer item_hits;
	private Integer item_stars;

	public Integer getItem_num() {
		return item_num;
	}

	public void setItem_num(Integer item_num) {
		this.item_num = item_num;
	}

	public String getItem_subject() {
		return item_subject;
	}

	public void setItem_subject(String item_subject) {
		this.item_subject = item_subject;
	}

	public String getItem_writer() {
		return item_writer;
	}

	public void setItem_writer(String item_writer) {
		this.item_writer = item_writer;
	}

	public String getItem_date() {
		return item_date;
	}

	public void setItem_date(String item_date) {
		this.item_date = item_date;
	}

	public Integer getItem_help() {
		return item_help;
	}

	public void setItem_help(Integer item_help) {
		this.item_help = item_help;
	}

	public Integer getItem_hits() {
		return item_hits;
	}

	public void setItem_hits(Integer item_hits) {
		this.item_hits = item_hits;
	}

	public Integer getItem_stars() {
		return item_stars;
	}

	public void setItem_stars(Integer item_stars) {
		this.item_stars = item_stars;
	}

}
